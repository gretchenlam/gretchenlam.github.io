import { readdirSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const albums = [
  { id: "favorites", title: "Favorites", sidebarLabel: "Favorites" },
  { id: "travel", title: "Travel", sidebarLabel: "Travel" },
  { id: "hobbies", title: "Hobbies", sidebarLabel: "Hobbies" },
];

const imageExtensions = new Set([
  ".avif",
  ".gif",
  ".jpeg",
  ".jpg",
  ".png",
  ".webp",
]);

const photosRoot = join(process.cwd(), "public", "img", "photos");
const outputPath = join(process.cwd(), "src", "desktop", "photoLibrary.generated.ts");

function toAltText(filename) {
  return filename
    .replace(/\.[^.]+$/, "")
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function getAlbumPhotos(albumId) {
  const albumPath = join(photosRoot, albumId);

  let files = [];

  try {
    files = readdirSync(albumPath, { withFileTypes: true });
  } catch {
    return [];
  }

  return files
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((filename) =>
      imageExtensions.has(filename.slice(filename.lastIndexOf(".")).toLowerCase())
    )
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
    .map((filename) => ({
      src: relative(join(process.cwd(), "public"), join(albumPath, filename)).replaceAll(
        "\\",
        "/"
      ),
      alt: toAltText(filename),
    }));
}

const sections = albums.map((album) => ({
  ...album,
  photos: getAlbumPhotos(album.id),
}));

const generated = `// Auto-generated by npm run photos. Edit files in public/img/photos/* instead.
export type PhotoItem = {
  src: string;
  alt: string;
};

export type PhotoSection = {
  id: "favorites" | "travel" | "hobbies";
  title: string;
  sidebarLabel: string;
  photos: PhotoItem[];
};

export const photoSections: PhotoSection[] = ${JSON.stringify(sections, null, 2)};
`;

writeFileSync(outputPath, generated);

console.log(
  `Generated ${relative(process.cwd(), outputPath)} with ${sections.reduce(
    (count, section) => count + section.photos.length,
    0
  )} photos.`
);
